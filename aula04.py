# Tipos int e float
# int -> Número inteiro
# o tipo int representa qualquer número positivo ou negativo
# int sem nenhum sinal '+' ou '-' é considerado positivo 
print(type(11))
print(-11)
print(0)

# float - Número com ponto flutuante 
# o tipo float representa qualquer número positivo ou negativo
# com ponto flutuante, float sem nenhum sinal '+' ou '-' é considerado positivo 
print(type(1.1))
print(-1.1)

# A função type mostra o tipo que o Python 
# inferiu ao valor 
