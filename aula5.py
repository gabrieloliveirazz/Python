# Tipo de dado bool (boolean)
# Ao questionar algo em um programa
# Só existe duas respostas possíveis:
# Sim (true) e não (false)
# Existem vários operadores para questionar 
# Dentre eles o ==, que é o operador lógico 
# que questiona se um valor é igual a outro
print(10 == 10) 
print(10 == 1)
print(type(10 == 10))